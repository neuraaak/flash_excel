#!/usr/bin/env python3
# ///////////////////////////////////////////////////////////////
# BUILD - Compile flash-excel using ezcompiler + pyproject.toml config
# ///////////////////////////////////////////////////////////////

"""Build script for flash-excel.

Loads configuration from [tool.ezcompiler] + [project] in pyproject.toml,
then runs the full build pipeline: compile → zip.

Usage:
    uv run .scripts/build/build.py
    uv run .scripts/build/build.py --compiler PyInstaller
    uv run .scripts/build/build.py --no-zip
"""

from __future__ import annotations

# ///////////////////////////////////////////////////////////////
# IMPORTS
# ///////////////////////////////////////////////////////////////
# Standard library imports
import argparse
import shutil
import sys
from pathlib import Path

# Third-party imports
from ezcompiler import EzCompiler
from ezcompiler.services.config_service import ConfigService
from ezplog import Ezpl

# ///////////////////////////////////////////////////////////////
# CONSTANTS
# ///////////////////////////////////////////////////////////////

PROJECT_ROOT = Path(__file__).resolve().parents[2]

# ///////////////////////////////////////////////////////////////
# HELPERS
# ///////////////////////////////////////////////////////////////


def _copy_tufup_root(project_root: Path) -> None:
    """Copy tufup root.json into the compiled bundle (bin/tufup/metadata/).

    The root.json is generated by publish.py on the first release.
    Without it the auto-updater cannot verify TUF metadata signatures,
    but the app still runs normally.
    """
    src = project_root / ".tufup" / "repo" / "metadata" / "root.json"
    dst_dir = project_root / "dist" / "Flash-Excel" / "bin" / "tufup" / "metadata"
    if not src.exists():
        print(
            "[build] WARNING: .tufup/repo/metadata/root.json not found.\n"
            "         Run publish.py at least once to generate it.\n"
            "         The compiled app will start but auto-update won't work."
        )
        return
    dst_dir.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst_dir / "root.json")
    print(f"[build] tufup root.json copied to bundle ({dst_dir})")


# ///////////////////////////////////////////////////////////////
# MAIN
# ///////////////////////////////////////////////////////////////


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Build flash-excel application.")
    parser.add_argument(
        "--compiler",
        choices=["auto", "Cx_Freeze", "PyInstaller", "Nuitka"],
        default=None,
        help="Force a specific compiler (overrides pyproject.toml).",
    )
    parser.add_argument(
        "--no-zip",
        action="store_true",
        help="Skip ZIP archive creation after compilation.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()

    # -- Init logging (active le printer pour les progress bars ezplog) --
    Ezpl(hook_logger=True, hook_printer=True)
    Ezpl().set_level("DEBUG")  # Set global log level (can be overridden by config)

    # -- Load config from pyproject.toml [project] + [tool.ezcompiler] --
    cli_overrides: dict = {}
    if args.compiler:
        cli_overrides["compiler"] = args.compiler
    if args.no_zip:
        cli_overrides["zip_needed"] = False

    config = ConfigService.build_compiler_config(
        pyproject_path=PROJECT_ROOT / "pyproject.toml",
        cli_overrides=cli_overrides or None,
    )

    # -- Run pipeline with built-in progress tracking --
    compiler = EzCompiler(config)
    compiler.run_pipeline(
        console=config.console,
        compiler=config.compiler if config.compiler != "auto" else None,
        skip_zip=args.no_zip,
        skip_upload=True,
    )

    # -- Copy tufup root.json into the bundle if available --
    _copy_tufup_root(PROJECT_ROOT)

    return 0


if __name__ == "__main__":
    sys.exit(main())
